#include <stdio.h>

int main() 
{
    int arr[] = {9,3,7,5,1,8,2,4,6};  // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    int len = sizeof(arr)/sizeof(arr[0]);  // ๏ฟฝ๏ฟฝ๏ฟฝ้ณค๏ฟฝ๏ฟฝ
    
    // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝรฐ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝหซ๏ฟฝ๏ฟฝัญ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    for(int i=0; i<len-1; i++)          // ๏ฟฝ๏ฟฝใฃบ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        for(int j=0; j<len-1-i; j++)    // ๏ฟฝฺฒใฃบรฟ๏ฟฝึฑศฝฯด๏ฟฝ๏ฟฝ๏ฟฝ
            if(arr[j] > arr[j+1])      // วฐ>๏ฟฝ๏ฟฝ๏ฟฝ๒ฝปป๏ฟฝ
            {    
				int t=arr[j];
                arr[j]=arr[j+1];
                arr[j+1]=t;
            }
    
    // ๏ฟฝ๏ฟฝำก๏ฟฝ๏ฟฝ๏ฟฝ
    for(int i=0; i<len; i++) printf("%d ",arr[i]);

    return 0;
}